uint8_t* current_icon = (uint8_t* ) icon_menu;

void ili9341_mcu_if_handleScanline(uint16_t *line, int scanline) {
  lcd_wait_ready();
  
  // nearest neighbor: 200 -> 240
  // The lower status bar will not be strechted to keep the pixel perfect pixel art images
  // the upper "3D" part is scaled insted a little more but that's nearest neigbor anyways
  // This will not be a perfect aspect-ratio-keeping approach but keeps the nice pixel art
  // at the bottom... (and it's hardly noticable at the splash screen)
  cnt_extra_scl++;
  uint8_t a = 1;
  if ((scanline < (SCREENHEIGHT - 32)) && (cnt_extra_scl == 4) && (num_extra_scl < 40)) {
    cnt_extra_scl = 0;
    num_extra_scl++;
    a = 2;
  }

  for (int i = 0; i < (SCREENWIDTH); i++)
    buf[i] = line[i];

  if ((scanline > 5) && (scanline < 13)) {
    for (int i = 290; i < 307; i++) {
      if (current_icon[17 * (scanline - 6) + i - 290] == 1)
        buf[i] =  0x7bef;
      }
  }

  if (scanline == 0) {
    num_extra_scl = 0;
    cnt_extra_scl = 0;
  }
    
  for (int i = 0; i < a; i++)
    ili9341_mcu_write((void*) buf, SCREENWIDTH);
}
